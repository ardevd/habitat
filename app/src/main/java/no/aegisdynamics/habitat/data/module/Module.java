package no.aegisdynamics.habitat.data.module;

/**
 * Immutable model class for a Z-Way module
 */
public class Module {

    String author;
    String homepage;
    String icon;
    String moduleName;
    String id;
    String source;
    String type;
    String version;
    String maturity;
    String description;
    String location;

    public String getAuthor() {
        return author;
    }

    public String getHomepage() {
        return homepage;
    }

    public String getIcon() {
        return icon;
    }

    public String getModuleName() {
        return moduleName;
    }

    public String getId() {
        return id;
    }

    public String getSource() {
        return source;
    }

    public String getType() {
        return type;
    }

    public String getVersion() {
        return version;
    }

    public String getMaturity() {
        return maturity;
    }

    public String getDescription() {
        return description;
    }

    public String getLocation() {
        return location;
    }

}
